public class Empregado {
    private String cpf;
    private String nome;

    public Empregado(String nome, String cpf) {
        this.cpf = cpf;
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public String getNome() {
        return nome;
    }
    
    
    
}
